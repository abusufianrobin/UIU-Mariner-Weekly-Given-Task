def morning_wish(name):
    return f"Good Morning {name}! Wishing you a day full of sunshine and happiness."

def afternoon_wish(name):
    return f"Good Afternoon {name}! Hope your day is going well and filled with positivity."

def evening_wish(name):
    return f"Good Evening {name}! May your evening be relaxing and peaceful."

def night_wish(name):
    return f"Good Night {name}! Sleep well and have sweet dreams."

def birthday_wish(name):
    return f"Happy Birthday {name}! Wishing you a year filled with joy and success."

def anniversary_wish(name):
    return f"Happy Anniversary {name}! May your love continue to bloom and bring you joy."

